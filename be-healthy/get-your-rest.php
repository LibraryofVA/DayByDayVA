            <h2>
                Bedtime Routines</h2>
            <p>
                <img alt="bedtime mobile" src="img/bedtime.jpg"></p>
            <p>
                <strong>Bedtime Routines routine should include activities that will occur consistently every night, so when they begin, your little one’s body will know it is time to prepare for sleep. Make sure the bedtime routine can be used consistently, even in your absence.</strong></p>
            <p>
                <strong>Young children respond more readily to sounds, smells and touch. The following are some suggested activities for the bedtime routine:</strong></p>
            <ul>
                <li>
                    Give your child a warm bath.</li>
                <li>
                    Use the same body lotion after bath time.</li>
                <li>
                    Read books to/with your child.</li>
                <li>
                    Rock your child to calm and relax him/her.</li>
                <li>
                    Sing songs with your child.</li>
                <li>
                    Say prayers with your child.</li>
                <li>
                    Snuggle with your child.</li>
                <li>
                    Play soft music in your child’s room.</li>
                <li>
                    Rub your child’s back.</li>
            </ul>
            <p>
                <em>From <a href="http://www.nebraskafamilyhelpline.ne.gov/behavioral-problems/" target="_blank">Nebraska DHHS</a></em></p>
        </div>
