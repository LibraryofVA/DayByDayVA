            <h2>
                Physically Active Children</h2>
            <p>
                <img alt="Active Kids" src="img/activekids.jpg"></p>
            <p>
                <strong>Children need to be physically active for at least thirty minutes a day. Here are some suggestions for some simple games and activities to help you get active with your child and have fun doing it!</strong></p>
            <ul>
                <li>
                    Head, Shoulders, Knees , and Toes</li>
                <li>
                    Duck, Duck, Goose</li>
                <li>
                    Hokey Pokey</li>
                <li>
                    Ring Around the Rosie</li>
                <li>
                    Hopscotch (toddlers may need to jump with both feet instead of one)</li>
                <li>
                    Tag</li>
                <li>
                    Hide and Seek (indoors)</li>
                <li>
                    Dancing to music</li>
                <li>
                    Taking a walk</li>
                <li>
                    Throwing or rolling a ball back and forth</li>
            </ul>
        </div>
        <p>
            <strong>Exercise is very important, even for toddlers and preschool children. Exercise strengthens bone and muscle development, lowers the risk of children becoming overweight, lowers blood pressure and bad cholesterol, decreases the risk of juvenile diabetes, and helps increase children's confidence and self-esteem.</strong></p>
