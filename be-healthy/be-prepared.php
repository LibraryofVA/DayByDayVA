            <h2>
                Survival Kit</h2>
            <p>
                <img alt="Active Kids" src="img/activekids.jpg"></p>
            <p>
                <strong>You may have to leave your house during a disaster and may sleep somewhere else for a while. It’s smart to put together your own Kid’s Activity Survival Kit so you will have things to do and share with other kids. These can all be stored in a backpack or duffel bag. Just make sure you can carry it easily. Some suggested items for your Activity Survival Kit:</strong></p>
            <ul>
                <li>
                    A few of your favorite books</li>
                <li>
                    Crayons, pencils or marking pens and plenty of paper</li>
                <li>
                    Scissors and glue</li>
                <li>
                    Two favorite toys such as a doll or action figure</li>
                <li>
                    One or two board games</li>
                <li>
                    A deck of cards</li>
                <li>
                    A puzzle (One with lots of pieces is good -- it takes a long time to do!)</li>
                <li>
                    Small people figures and play vehicles that you can use to play out what is happening during your disaster -- such as ambulance, fire truck, helicopter, dump truck, police car, small boats.</li>
                <li>
                    Favorite stuffed animal or puppet</li>
                <li>
                    Favorite blanket or pillow</li>
                <li>
                    Pictures of the family and pet</li>
                <li>
                    A "keep safe" box with a few treasures that make you feel special.</li>
            </ul>
            <p>
                <em>From <a href="http://www.fema.gov/kids/" target="_blank">FEMA for Kids</a></em></p>
        </div>
