            <h2>Wash your hands!</h2>
            <p>
                <img alt="wash your hands" src="img/washhands.jpg"></p>
            <p>
                <strong>When do I wash?</strong></p>
            <ul>
                <li>
                    Before eating.</li>
                <li>
                    After using the bathroom (or helping your child do so, or changing a diaper).</li>
                <li>
                    After playing outside or with pets.</li>
                <li>
                    Any time hands look dirty.</li>
            </ul>
            <p>
                <strong>How do I wash?</strong></p>
            <ul>
                <li>
                    Use warm water and soap.</li>
                <li>
                    Wet hands and apply soap.</li>
                <li>
                    Rub your hands with the soap. Make sure you show your child how to rub soap on all areas of the hand, including the backs, in between fingers, and under nails.</li>
                <li>
                    Rinse hands with warm water and dry with a towel.</li>
                <li>
                    If you are using a public restroom, use the paper towel to turn off the faucet.</li>
            </ul>
            <p>
                <strong>How long should I wash?</strong></p>
            <ul>
                <li>
                    Wash for about 15 seconds. You can count the seconds, OR sing "Happy Birthday" twice</li>
            </ul>
