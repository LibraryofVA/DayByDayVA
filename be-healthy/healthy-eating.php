            <h2>
                Family Meal Tips</h2>
            <p>
                <img alt="eating" src="img/eating.jpg"></p>
            <p>
                <strong>Whether you have a toddler or a teen, here are five of the best strategies to improve nutrition and encourage smart eating habits:</strong></p>
            <ul>
                <li>
                    Have regular family meals.</li>
                <li>
                    Serve a variety of healthy foods and snacks.</li>
                <li>
                    Be a role model by eating healthy yourself.</li>
                <li>
                    Avoid battles over food.</li>
                <li>
                    Involve kids in the process.</li>
            </ul>
            <p>
                <em>From <a href="http://kidshealth.org/parent/nutrition_fit/nutrition/habits.html" target="_blank">KidsHealth.org</a></em></p>
        </div>
