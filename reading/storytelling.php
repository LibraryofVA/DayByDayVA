      <p class="heading blueText padded">Tips for Reading Out Loud</p>
      <div class="feature_box" class="padded">
        <p><img alt="read together" src="/reading/img/readtogether.jpg"></p>
      </div>
      <p class="padded"><strong>Remember that reading is fun and sharing time together is one of the best gifts you can give your child.</strong></p>
      <ul class="booklist">
        <li>
            <strong>Set the Mood:</strong> When you are ready for your story time, make yourself and your child comfortable. Let them know you are ready. Perhaps let them choose the stories. It is good to try and get into a routine of when you might read together, such as at bedtime. You and your child should not be too active or too tired.</li>
        <li>
            <strong>Preparation:</strong> Look over the book before reading it, so you feel comfortable with it. Hold the book the correct way, right side up, so the child can see.</li>
        <li>
            <strong>Expression:</strong> Change your voice for different characters—use accents or inflection. Let your voice get soft and loud. Put some life and energy into your reading.</li>
        <li>
            <strong>Have Patience:</strong> Take time to answer your child's questions. Their questions mean they are engaged in the story. It’s ok if your young child can’t sit still through the story. A child who moves around or plays with a quiet toy may still be hearing every word.</li>
        <li>
            <strong>Participation:</strong> Let them repeat phrases with you or leave out a word here and there and see if they can fill in the blank. You can also change up the story by not reading it word for word but use the pictures to help you tell the story. This is also a good way to ask your child what is happening or what happens next.</li>
        <li>
            <strong>Pace:</strong> Don't race while reading. Take your time. If time is a problem, read a shorter story.</li>
      </ul>
      <p class="heading blueText padded">Storytelling Tips</p>
      <p class="padded"><strong>For those of you who want to engage your child in storytelling, here are a few tips.</strong></p>
      <ul class="booklist">
        <li>
            Allow your child to help select the book or story; you may enjoy using fairy tales and folk tales.</li>
        <li>
            Make sure you are familiar with the order of the story. You can always enhance a story but you should know by heart the general storyline.</li>
        <li>
            If you are going to tell stories to a child or group of children, you might want to practice in front of a mirror. If your child wants to tell you a story, just let them go, encouraging them and assisting their confidence.</li>
        <li>
            Do act out the characters! The more you can use voices, inflection, or facial expressions, the more you can engage your child.</li>
        <li>
            Whether you are telling a story for one child or many, feel free to bring them into the story. You can use their names or ask them what they think happens next.</li>
        <li>
            Enjoy yourself and have fun!</li>
      </ul>
      <p class="heading blueText padded">Sharing Books with Babies</p>
      <ul class="booklist">
        <li>
            Pick a time when you and your child are in a good mood, ready to enjoy your time together.</li>
        <li>
            Choose a quiet spot. Turn off the TV and the radio. Your voice is your child’s favorite sound! In particular, babies enjoy a soft, sing-song voice.</li>
        <li>
            Don’t worry if you think you are not a good reader yourself. The books for young children are not hard. Even if you just talk about the pictures, you and your child will gain enjoyment from the experience of reading together. If you enjoy the books, so will your child!</li>
        <li>
            Hold your baby in your lap and be sure he or she can see the pages. Feel free to let your baby interact. Ask questions and point out objects.</li>
        <li>
            Do not be upset when your baby grabs the book and begins to chew on it. That is normal! Babies like to play with books and this is how they get to know the book—by chewing on it, throwing it down, and playing with it. It is perfectly natural. If your baby puts a book in his or her mouth, you might try to gently and slowly take the book away from the baby’s mouth and cheerfully say, “Let’s see what’s in this book!” If your baby just wants to play with the book, that’s fine. Perhaps you can try reading it together later.</li>
        <li>
            Since babies are still developing, sometimes they have trouble focusing. Board books are popular for babies because they are small and sturdy and made with a heavier cardboard. As babies learn to grasp things, pick light, washable, cloth or vinyl books.</li>
        <li>
            The books you choose should have bright, colorful pictures, geometric shapes, and clear pictures including human faces with high contrast between the picture and the background.</li>
        <li>
            As babies become interested in what's inside a book, you can choose books with familiar objects or routine events like taking a bath or going to bed. Older babies like books with textures, things to touch, or flaps to lift.</li>
        <li>
            Babies of any age enjoy homemade books and family photo albums.</li>
        <li>
            Spend time every day reading a book with your baby. Let your baby play with a book. Keep a book in your diaper bag so you can pull it out when it is convenient. You can read more than once a day!</li>
        <li>
            Reading out loud helps babies to associate reading with warm, pleasant feelings and sounds and rhythm, especially through rhymes like Mother Goose and poetry. Babies make sounds first — gurgling, cooing — which eventually become talking. They point, touch, and chew on the books which help to develop their senses.</li>
      </ul>
